// Methods in js


// PUSH :-  Add to end  -----------------

let Vegitable = ["Aloo","Tamatar","Gobhi","kaddu","Bhindi"];
Vegitable.push("palak");
console.log(Vegitable);

// UNSHIFT :- Add to start  --------------

Vegitable.unshift("Muli");
console.log(Vegitable);

// POP :- delete from end  ----------------

Vegitable.pop()
console.log(Vegitable)


// SHIFT :- Delete from start  ----------------

Vegitable.shift()
console.log(Vegitable);